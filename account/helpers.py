def get_month_name(num_of_month):
    if num_of_month == 1:
        name = 'January'
    elif num_of_month == 2:
        name = 'February'
    elif num_of_month == 3:
        name = 'March'
    elif num_of_month == 4:
        name = 'April'
    elif num_of_month == 5:
        name = 'May'
    elif num_of_month == 6:
        name = 'June'
    elif num_of_month == 7:
        name = 'July'
    elif num_of_month == 8:
        name = 'August'
    elif num_of_month == 9:
        name = 'September'
    elif num_of_month == 10:
        name = 'October'
    elif num_of_month == 11:
        name = 'November'
    elif num_of_month == 12:
        name = 'December'
    else:
        name = num_of_month
    return name
