def get_month_name(num_of_month):
    if num_of_month == '01':
        name = 'January'
    elif num_of_month == '02':
        name = 'February'
    elif num_of_month == '03':
        name = 'March'
    elif num_of_month == '04':
        name = 'April'
    elif num_of_month == '05':
        name = 'May'
    elif num_of_month == '06':
        name = 'June'
    elif num_of_month == '07':
        name = 'July'
    elif num_of_month == '08':
        name = 'August'
    elif num_of_month == '09':
        name = 'September'
    elif num_of_month == '10':
        name = 'October'
    elif num_of_month == '11':
        name = 'November'
    elif num_of_month == '12':
        name = 'December'
    else:
        name = num_of_month
    return name
